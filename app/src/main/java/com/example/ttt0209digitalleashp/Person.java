package com.example.ttt0209digitalleashp;

public class Person {

    private String fullName;

    // constructor
    public Person(String fullName) {
        this.fullName = fullName;
    }
    // END constructor

    // getter & setters
    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }
    // END getters & setters
}
